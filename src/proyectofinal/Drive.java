    public void ModelolistIntegrantes(Object t) {
        DefaultListModel modelo
                = (DefaultListModel) jl_personas.getModel();
        modelo.clear();
        for (Grupo gr : GruposI) {
            if (gr == t) {
                for (Integer integrante : gr.getIntegrantes()) {
                    modelo.addElement(integrante);
                }
            }
        }

        jl_personas.setModel(modelo);
        modelocombobox();
    }


    public void modeloTABLEListar() {
        DefaultTableModel listPersona = (DefaultTableModel) tabla.getModel();
        listPersona.setRowCount(0);

        for (Archivo archivo : ArchivosI) {
            if (archivo.getIdes().get(0) == NewJFrame.Uactual.getID() && archivo.getPapelera().equals("No")) {
                Object[] row = {archivo.getArch().getName(), archivo.getFecha(), NewJFrame.Uactual.getUsuario()};
                listPersona.addRow(row);
            }
        }
        tabla.setModel(listPersona);
    }


    private void AgregarArchivoBoton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarArchivoBoton1ActionPerformed
        JFileChooser jfc = new JFileChooser();//Instqncia fc

        int seleccion = jfc.showSaveDialog(this);  //mostrar la ventana de dialogo      
        FileWriter fw = null;
        BufferedWriter bw = null;
        if (seleccion == JFileChooser.APPROVE_OPTION) {//si preciono okay
            try {

                File fichero = null;//crear instancia file nula, la cual se le agregara una extns
                fichero = jfc.getSelectedFile();
                fw = new FileWriter(fichero);//wilewritter a archivo
                bw = new BufferedWriter(fw);//bufferded writter a el canar fw
                ArchivosI.add(new Archivo(fecha(), fichero, NewJFrame.Uactual.getID()));
//                bw.write(ta_1.getText());//escribe el contenido de el area de tecto
//                ta_1.setText("");
                bw.flush(); //flashea, vacia de ram a rom        
                JOptionPane.showMessageDialog(this,
                        "Archivo guardado exitosamente");

            } catch (Exception e) {
                e.printStackTrace();
            }
            try {
                bw.close();
                fw.close();
            } catch (IOException ex) {

            }
            modeloTABLEListar();
        }

    }//GEN-LAST:event_AgregarArchivoBoton1ActionPerformed


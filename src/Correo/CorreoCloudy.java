    private void GuardarDraftMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_GuardarDraftMouseClicked
        boolean ver = true;
        int id = 0;
        for (Correo correo : correos) {
            if (correo.getAsunto().equals(Subject.getText()) || correo.getCuerpo().equals(Body.getText()) || correo.getRecipiente().equals(Aquienenvia.getText())) {
                ver = false;
                id = correo.getIdCorreo();
            }
        }
        if (ver) {
            ponerBorrador();
            Body.setText("");
            Aquienenvia.setText("");
            Subject.setText("");
            EnviarCuadrito.setVisible(false);
            JOptionPane.showMessageDialog(this, "Borrador Guardado", "Exito", JOptionPane.INFORMATION_MESSAGE);
        } else {
            updateBorrador(id);
            Body.setText("");
            Aquienenvia.setText("");
            Subject.setText("");
            EnviarCuadrito.setVisible(false);
            JOptionPane.showMessageDialog(this, "Borrador Actualizado", "Exito", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_GuardarDraftMouseClicked


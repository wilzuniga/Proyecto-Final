    private void sendMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sendMouseClicked

        Server server = new Server("smtp.office365.com", "587", Usser, Contraseña);
        server.conectar();
        try {
            LinkMail mail = new LinkMail(server.getSession());
            mail.addRecipient(Message.RecipientType.TO, new InternetAddress(Aquienenvia.getText()));
            mail.setSubject(Subject.getText());
            mail.setText(Body.getText());
            BodyPart parteTexto = new MimeBodyPart();
            parteTexto.setContent("<b>" + Body.getText() + "</b>", "text/html");

            MimeMultipart todaslasPartes = new MimeMultipart();
            todaslasPartes.addBodyPart(parteTexto);
            mail.setContent(todaslasPartes);
            mail.setFrom(server.getUsuario());

            server.enviarCorreo(mail);
            JOptionPane.showMessageDialog(this, "Correo Enviado", "Exito", JOptionPane.INFORMATION_MESSAGE);

            Body.setText("");
            Aquienenvia.setText("");
            Subject.setText("");
            EnviarCuadrito.setVisible(false);
        } catch (Exception e) {
        }

    }//GEN-LAST:event_sendMouseClicked


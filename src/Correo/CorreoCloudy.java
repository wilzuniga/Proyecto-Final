    public boolean Conectandoconlosdiosesdelasmatematicas() {
        try {
            session = javax.mail.Session.getInstance(props, new javax.mail.Authenticator(){
                protected PasswordAuthentication getPasswordAuthentication(){
                    return new PasswordAuthentication(Usser,Contraseña);
                }
            });
            store = session.getStore("imap");
            store.connect("outlook.office365.com", Usser, Contraseña);

            BandejadeEntrada = store.getFolder("INBOX");
            BandejadeEntrada.open(Folder.READ_WRITE);
            inboxM = BandejadeEntrada.getMessages();

            ElementosEnviados = store.getFolder("Elementos enviados");
            ElementosEnviados.open(Folder.READ_WRITE);
            enviadosM = ElementosEnviados.getMessages();

            ElementoEliminados = store.getFolder("Elementos eliminados");
            ElementoEliminados.open(Folder.READ_WRITE);
            eliminadosM = ElementoEliminados.getMessages();

            MierdaSpam = store.getFolder("Correo no deseado");
            MierdaSpam.open(Folder.READ_WRITE);
            spamM = MierdaSpam.getMessages();
            return true;
        } catch (Exception e) {
            System.out.println(Usser + " " + Contraseña);
            
        }
        return false;
    }

